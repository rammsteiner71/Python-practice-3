def month_name(number, language):
    if language == "ru":
        if number == 1:
            return "Январь"
        elif number == 2:
            return "Февраль"
        elif number == 3:
            return "Март"
        elif number == 4:
            return "Апрель"
        elif number == 5:
            return "Май"
        elif number == 6:
            return "Июнь"
        elif number == 7:
            return "Июль"
        elif number == 8:
            return "Август"
        elif number == 9:
            return "Сентябрь"
        elif number == 10:
            return "Октябрь"
        elif number == 11:
            return "Ноябрь"
        elif number == 12:
            return "Декабрь"
    elif language == "en":
        if number == 1:
            return "January"
        elif number == 2:
            return "February"
        elif number == 3:
            return "March"
        elif number == 4:
            return "April"
        elif number == 5:
            return "May"
        elif number == 6:
            return "June"
        elif number == 7:
            return "July"
        elif number == 8:
            return "August"
        elif number == 9:
            return "September"
        elif number == 10:
            return "October"
        elif number == 11:
            return "November"
        elif number == 12:
            return "December"